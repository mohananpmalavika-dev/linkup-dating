export const languageOptions = Object.entries(translations).map(([code, value]) => ({
  code,
  name: value.languageName,
}));

export const getTranslation = (language) => translations[language] || translations.en;

export const getTranslationValue = (language, path, fallback = "") => {
  const readPath = (source) =>
    path.split(".").reduce(
      (currentValue, key) => (currentValue && currentValue[key] !== undefined ? currentValue[key] : undefined),
      source
    );

  const selectedValue = readPath(getTranslation(language));
  if (selectedValue !== undefined) {
    return selectedValue;
  }

  const englishValue = readPath(translations.en);
  return englishValue !== undefined ? englishValue : fallback;
};

export default translations;
